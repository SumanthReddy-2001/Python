a = int(input("Enter a value: "))
b = int(input("Enter b value: "))
operator =input("enter operator: ")
if operator == "+":
    print("result=",a+b)
elif operator =="-":
    print("result=",a-b)
elif operator =="*":
    print("result=",a*b)
elif operator =="%":
    print("result=",a%b)
elif operator =="/":
    print("result=",a/b)
else:
    print("no")